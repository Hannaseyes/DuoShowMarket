/**
 * 品牌类型实体类
 * @author Wang Hanqing
 */
public class BrandType {
	private Integer id;
	private String brandType;
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getBrandType() {
		return brandType;
	}
	public void setBrandType(String brandType) {
		this.brandType = brandType;
	}
}
